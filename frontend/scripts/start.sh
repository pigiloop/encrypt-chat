    log_info "–î–∏—Ä–µ–∫—Ç–æ—Ä–∏—è dist –¥–æ–ª–∂–Ω–∞ —Å–æ–¥–µ—Ä–∂–∞—Ç—å –≥–æ—Ç–æ–≤—ã–µ —Ñ–∞–π–ª—ã"
