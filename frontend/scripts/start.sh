    log_error "npm –Ω–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω!"
    log_info "–£—Å—Ç–∞–Ω–æ–≤–∏—Ç–µ npm (–æ–±—ã—á–Ω–æ –∏–¥–µ—Ç —Å Node.js)"
