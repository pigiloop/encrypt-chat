    log_info "–î–∏—Ä–µ–∫—Ç–æ—Ä–∏—è node_modules –Ω–∞–π–¥–µ–Ω–∞, –ø—Ä–æ–ø—É—Å–∫–∞–µ–º —É—Å—Ç–∞–Ω–æ–≤–∫—É"
